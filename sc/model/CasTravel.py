  applyperson = scrapy.Field()    # 申请人
  applyDate = scrapy.Field()    # 申请日期
  MergeCode = scrapy.Field()    # 合并代码
  ApprovalData = scrapy.Field()    # 审批信息
  CasDriverDID = scrapy.Field()    # 司机model关联ID
  CasBusinessCarDID = scrapy.Field()    # 车辆model关联id
  cqf = scrapy.Field()    # 路桥费
  Kilometers = scrapy.Field()    # 总公里数
  Remark = scrapy.Field()    # 备注
  TotalUseTime  = scrapy.Field()    # 行程总用时
  Types  = scrapy.Field()    # 类型：多日期行程的类型
