  UseDate = scrapy.Field()    # 用车日期
  CarType = scrapy.Field()    # 用车类型
  UseReson = scrapy.Field()    # 用车理由
  DetailedAddress = scrapy.Field()    # 行程详细地址
  Company = scrapy.Field()    # 公司
  CostDepartment = scrapy.Field()    # 经费负担部门
  ShareCore = scrapy.Field()    # 经费负担代码
  CreateID = scrapy.Field()  
  CreateDate = scrapy.Field()  
  ModifyDate = scrapy.Field()  
  ModifyID = scrapy.Field()  
  Enables = scrapy.Field()    # 是否可用：用于计算分摊标志
